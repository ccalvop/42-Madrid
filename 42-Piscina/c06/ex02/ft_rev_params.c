#include <unistd.h>

void	ft_putstr(char *str)
{
	while (*str)
		write(1, str++, 1);
}

int	main(int argc, char **argv)
{
	int	i;

	if (argc > 1)
	{
		i = 1;
		while (i < argc)
		{
			ft_putstr(argv[argc - i]);
			write(1, "\n", 1);
			i++;
		}
	}
	return (0);
}
